package com.dicoding.javafundamental.operator;

public class operatorAritmatika {
    public static void main(String[] args){
        System.out.println("Operasi penjumlahan");
        int hasilPenjumlaha = 5 + 1;
        System.out.println("Hasil dari 5 + 1 = " + hasilPenjumlaha);
        System.out.println();

        System.out.println("Operasi Pengurangan");
        int hasilPengurangan = 4 - 1;
        System.out.println("Hasil dari 4 - 1 = " + hasilPengurangan);
        System.out.println();

        System.out.println("Operasi Pengalian");
        int hasilPerkalian = 5 * 5;
        System.out.println("Hasil dari 5 * 5 = " + hasilPerkalian);
        System.out.println();

        System.out.println("Operasi Pembagian");
        int hasilBagi = 20 / 2;
        System.out.println("Hasil dari 20 / 2 =" + hasilBagi);
        System.out.println();

        System.out.println("Operasi sisa bagi");
        int sisaBagi = 8 % 2;
        System.out.println("Hasil dari 8 % 2 =" + sisaBagi);
        System.out.println();
        int sisaBagi2 = 9 % 2;
        System.out.println("Hasil dari 9 % 2 =" + sisaBagi2);
        System.out.println();
    }
}
